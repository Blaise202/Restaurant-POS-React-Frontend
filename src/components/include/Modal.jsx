import React from "react";

const Modal = ({ title, onClose, isOpen, children }) => {
  if (!isOpen) return null;
  return (
    <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
      <div className="bg-[#1a1a1a] w-full rounded-lg p-4 shadow-lg max-w-lg mx-4">
        <div className="flex justify-center items-center px-6 py-4 border-b border-[#333]">
          <h2 className="text-xl text-[#f5f5f5] font-semibold">{title}</h2>
          <button
            onClick={onClose}
            className="ml-auto text-gray-500 hover:text-gray-300 text-2xl"
          >
            &times;
          </button>
        </div>
        <div className="p-6 text-[#f5f5f5]">{children}</div>
      </div>
    </div>
  );
};

export default Modal;
